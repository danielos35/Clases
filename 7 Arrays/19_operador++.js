// ...........................++...................
let numero = 3; 
console.log(numero++);
// El operador ++ incrementa el valor para la proxima vez que sea llamado
console.log(numero++);


// Para incrementarlo de inmediato se debe de colar el operador antes de la variable
console.log(++numero);

// .............................--...................
console.log(numero--);

// igual que el operador -- decrementa el valor en la proxima vez que este sea llamado   
console.log(numero--);




// ............................+=.....................
// Es usado para sumar con otra variable 
console.log(numero+=2);
console.log(numero-=2);